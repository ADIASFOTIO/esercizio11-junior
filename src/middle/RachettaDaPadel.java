package middle;

public class RachettaDaPadel {
    private String marca;
    private double peso;
    private double costo;
    public RachettaDaPadel(){

    }
    public RachettaDaPadel(String marca, double peso,double costo){
        this.marca = marca;
        this.peso = peso;
        this.costo = costo;
    }
    public double getPeso() {
        return peso;
    }
    public String getMarca() {
        return marca;
    }
    public double getCosto() {
        return costo;
    }








}
